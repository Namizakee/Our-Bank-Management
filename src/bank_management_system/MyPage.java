    public void displayCredit()
    {
        try
        {
            if (cmbBox_CreditTrans.getSelectedItem().toString() == "Select an account")
            {
                txtField_CreditATrans.setText("");
            }
            else
            {
                String sql = "SELECT Balance FROM balances WHERE Name = '"+ cmbBox_CreditTrans.getSelectedItem().toString() +"'";
                Statement stmt = conn.createStatement();
                rs = stmt.executeQuery(sql);

                if (rs.next())
                {
                    int item = rs.getInt("Balance");

                    txtField_CreditATrans.setText(String.valueOf(item));
                }
            }
        } catch (SQLException ex)
        {
            ex.printStackTrace();
        }
    }
    
