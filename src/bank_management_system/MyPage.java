    private void btn_TransferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TransferActionPerformed
        try
        {
            int prompt = JOptionPane.showConfirmDialog(this, "Are you sure you want to transfer?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (prompt == JOptionPane.YES_OPTION)
            {
                if (txtField_UserTrans.getText().isBlank())
                {
                    JOptionPane.showMessageDialog(this, "Fill in to search a user", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                else if (txtField_BalanceTrans.getText().isBlank())
                {
                    JOptionPane.showMessageDialog(this, "Transfer Failed", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                else if (txtField_AmountTrans.getText().isBlank())
                {
                    JOptionPane.showMessageDialog(this, "Enter a transfer amount", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                else if (txtField_CreditATrans.getText().isBlank())
                {
                    JOptionPane.showMessageDialog(this, "Select a credit account", "Warning", JOptionPane.WARNING_MESSAGE);
                }

                int availBal = Integer.valueOf(txtField_BalanceTrans.getText());
                int remainingBal = availBal - Integer.valueOf(txtField_AmountTrans.getText());
                int addBal = Integer.valueOf(txtField_AmountTrans.getText()) + Integer.valueOf(txtField_CreditATrans.getText());
                int transAmount = Integer.valueOf(txtField_AmountTrans.getText());

                if (transAmount > availBal)
                {
                    JOptionPane.showMessageDialog(this, "Transfer amount cannot be greater than the available balance", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                    String sqlAcc = "UPDATE account set Balance = '"+ remainingBal +"' WHERE Name = '"+ txtField_UserTrans.getText() +"'";
                    String sqlBal = "UPDATE balances set Balance = '"+ remainingBal +"' WHERE Name = '"+ txtField_UserTrans.getText() +"'";
                    String sqlAccAdd = "UPDATE account set Balance = '"+ addBal +"' WHERE Name = '"+ cmbBox_CreditTrans.getSelectedItem().toString() +"'";
                    String sqlBalAdd = "UPDATE balances set Balance = '"+ addBal +"' WHERE Name = '"+ cmbBox_CreditTrans.getSelectedItem().toString() +"'";

                    Statement stmt = conn.createStatement();
                    stmt.executeUpdate(sqlAcc);
                    stmt.executeUpdate(sqlBal);
                    stmt.executeUpdate(sqlAccAdd);
                    stmt.executeUpdate(sqlBalAdd);

                    JOptionPane.showMessageDialog(this, "Transfer Successfully", "Message", JOptionPane.INFORMATION_MESSAGE);
                    txtField_UserTrans.setText("");
                    txtField_NameTrans.setText("");
                    txtField_AccTrans.setText("");
                    txtField_BalanceTrans.setText("");
                    txtField_AmountTrans.setText("");
                    txtField_CreditATrans.setText("");
                    cmbBox_CreditTrans.removeAllItems();
                    cmbBox_CreditTrans.addItem("Select an account");
                }
            }
        } catch (SQLException ex)
        {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btn_TransferActionPerformed

