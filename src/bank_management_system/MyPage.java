    public void showCustomerTable()
    {
                
        try
        {
            DefaultTableModel model = (DefaultTableModel)table_Customer.getModel();
            String sql = "SELECT Acc, Name, Acc_Type, Nationality, Caste, Gender, Mob, Address FROM account";
            Statement stmt = conn.createStatement();
            rs = stmt.executeQuery(sql);
            ResultSetMetaData rsmd = rs.getMetaData();
            
            int cols = rsmd.getColumnCount();
            String[] colName = new String[cols];
            for (int i = 0; i < cols; i++)
                colName[i] = rsmd.getColumnName(i+1);
            model.setColumnIdentifiers(colName);
            
            while (rs.next())
            {
                String Acc = rs.getString("Acc");
                String Name = rs.getString("Name");
                String Acc_Type = rs.getString("Acc_Type");
                String Nationality = rs.getString("Nationality");
                String Caste = rs.getString("Caste");
                String Gender = rs.getString("Gender");
                int Mobile = rs.getInt("Mob");
                String Address = rs.getString("Address");
                
                String[] list = {Acc, Name, Acc_Type, Nationality, Caste, Gender, String.valueOf(Mobile), Address};
                model.addRow(list
                );
            }
        } catch (SQLException ex)
        {
            ex.printStackTrace();
        }
    }
    
