    private boolean checkStatus(JTextField[] txtFields)
    {
        boolean isTrue = true;
        for (JTextField fields : txtFields)
        {
            if (!fields.isEnabled())
                isTrue = false;
        }
        
        return isTrue;
    }
    
    private boolean checkBlankFields(String[] txtFields)
    {
        boolean isTrue = false;
        for (String fields : txtFields)
        {
            if (fields.isBlank())
                isTrue = true;
        }
        
        return isTrue;
    }
